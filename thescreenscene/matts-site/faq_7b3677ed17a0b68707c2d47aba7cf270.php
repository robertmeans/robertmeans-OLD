  <a name="01" class="faqAnswerHeader">Do I have to talk to the police?</a>
    
  <p>If you are being detained, the law requires that you give your name. Beyond that you don't have to say anything.</p>
  
 <p><a name="02" class="faqAnswerHeader">How do I know if I'm being detained?</a></p>
  
  <p>If you have been stopped by the police on the street. Ask if you can leave. If the answer is yes, then leave. If the answer is no, you are being detained. If the police do not answer you, consider walking away. If they stop you, you are being detained.</p>
  
  <p>If you are stopped while driving, you are being detained.</p> 
  
  <p><a name="03" class="faqAnswerHeader">Do I have to talk to the police if they have a warrant to search and/or to arrest?</a></p>
  
  <p>No. A warrant does not change your right to remain silent. You may have to give your name if you are being detained or arrested while the warrant is being executed. However, you still have a right to remain silent. </p>
  
  <p><a name="04" class="faqAnswerHeader">Should I talk to the police?</a></p>
  
  <p>No. Anything you say can and will be used against you and others. You can't talk your way out of criminal charges. Explaining what happened will only give the District Attorney more evidence to prosecute you. </p>
  
  <p>It is difficult not to talk to the police for several reasons.  First, we are taught to cooperate with the police. Second, it is human nature to want to explain your side of the story. Lastly, most people will answer questions when asked nicely. Police are trained to find incriminating evidence, they are  professionals and they know we are conditioned to cooperate.  Police are trained to be nice. They will use many methods, including a friendly  tone or other suggestive tactics to get you to talk.</p>
  
  <p>Police will often say:</p>
  
  <p>"I just need your side of the story for my report."</p>
  <p>OR </p>
  <p>"Do you want to tell me what happened before you go to jail."</p>	
  <p>OR</p>
  <p>"I can tell the judge that you were cooperative."</p>
  
  <p>You can't talk your way out of going to jail. Don't give your side of the story to the police. The police are likely trying to build a criminal case against you. Tell  your lawyer your side of the story. Your lawyer will defend you and your rights.</p>  
  
  <p><a name="05" class="faqAnswerHeader">What do I say to the police to protect my rights?</a></p>
  
 <p>Besides giving your name, you should only say three things to the police:</p>
  
 <ol> 
    <li>Am I free to leave? If you are not free to leave then you should say:
        <li>I would like to remain silent; and </li>
        <li> I would like to speak to an attorney.</li>
    </li>
  </ol>
  <p>The police will often infer or imply that everything will be fine if you tell them what happened. Sometimes two officers will try to get to you to talk. One officer will be firm and authoritative while the other is friendly and helpful. This is called "good cop / bad cop". During an arrest emotions are high and fear can take over. Police are trained to play on your fears. Don't be fooled. </p>
  
  <p><a name="06" class="faqAnswerHeader">Should I lie to the police?</a></p>
  
  <p>No. Never lie to the police. Doing so is a crime. Do not give the police fake identification or a fake name. Many jurisdictions charge a fake identification or false a name as a felony.</p>
  
  <p>Give your name, tell them you wish to remain silent, and ask to speak with an attorney.</p> 
  
  <p><a name="07" class="faqAnswerHeader">Should I consent to a search or my car, house, or person?</a></p>
  
  <p>No. Never consent to a search of your car, house, or person. DO NOT USE FORCE or interfere. They may search despite your refusal. Simply tell them that you do not consent. The police need legal a reason to search your car, house, or person. If they have that reason they would not be asking you for consent to search.</p> 
  
  <p>If you give your consent, then tehy no longer need a legal reason to erform the serch. That means, whatever they find is likely going to be used to charge you with a crime. Even if you think they are eventually going to get a legal reason for the search, NEVER consent to a search of your car, home, or person.</p>
  
  <p>If they ask why you won't consent say:</p>
  
  <p>(1) I would like to remain silent;</p>
  
  <p>(2) I would like to speak with an attorney.</p> 
  
  <p><a name="08" class="faqAnswerHeader">Should I let the police into my house if they ask to come in?</a></p>
  
  <p>Never let the police into your home if they ask to come in. DO NOT USE FORCE or interfere. Be polite and tell them you don't want them in your house. </p>
  
  <p>Police may come in your house regardless of what you say. However, it is important to tell them that you are not giving them consent to enter. </p>
  
  <p>This same thing goes for hotel rooms, tents, and any place where you have a legitimate expectation of privacy.</p> 
  
  <p>If the police ask you why don't want them in your house say:</p> 
  
  <p>(1) I would like to remain silent;</p>
  
  <p>(2) I would like to speak with an attorney.</p>
  
  <p><a name="09" class="faqAnswerHeader">Should I consent to a search if the police have a warrant?</a></p>
  
  <p>Tell the police that you do not consent to a search. DO NOT USE FORCE or interfere. They will conduct the search despite your objection. Simply state that you do not consent to a search.</p>
  
  <p>You should call your lawyer as soon as possible. </p>
  
  <p><a name="10" class="faqAnswerHeader">Do I really need a lawyer?</a></p>
  
  <p>The law does not require you to have a lawyer. However, you should consider getting one. The law allows for people to represent themselves. If you represent yourself, you are a pro se party. If you are a pro se party, the Judge will treat you as if you have the same knowledge, skill, and training as a lawyer. In other words, if you mess up your case, you will be stuck with the consequences.</p>
  
  <p>Lawyers are not necessarily smarter than anyone else. However, lawyers are trained to think, read, write, and argue in a very specific way. Additionally, many lawyers carry malpractice insurance. That means if your lawyer commits malpractice you may be able to recover money damages from their insurance company. </p>
  
  <p>Ask your lawyer if they have malpractice insurance during your initial free consultation.</p>
  
  <p><a name="11" class="faqAnswerHeader">How much will my case cost?</a></p>
  
  <p>The cost of legal representation varies depending on the type of case. A misdemeanor or DUI may be resolved for as little as a few hundred dollars up to several thousand. Felony cases generally require as little as a thousand up to several thousand dollars. Violent felonies, sex offenses, and death penalty cases require significantly more. </p>
  
 <p>Much of the cost is determined by whether or not the case goes to trial, the amount of investigation, the number of court appearances, the length of trial, and whether your case requires the testimony of an expert witness.</p> 
  
  <p>Some lawyers offer an initial free consultation. Lawyers charged different rates. Speak with more than one lawyer and compare rates. </p>
  
  <p><a name="12" class="faqAnswerHeader">How much does investigation cost?</a></p>
  
  <p>Factors that determine the cost of investigation include: the number of witnesses and number of subpoenas; the complexity and number of charges; the volume and complexity of physical evidence. Investigation typically costs between $50.00 and $100.00 per hour. </p>
  
 <p>Ask your lawyer to estimate how much an investigation will cost during your initial free consultation. </p> 
  
  <p><a name="13" class="faqAnswerHeader">Do I need an expert witness?</a></p>
  
  <p>You may. It depends on the type of case you have and the issues the case presents. An expert witness is someone who has scientific, technical, or other specialized knowledge that will assist the jury or judge in understanding evidence or determining a fact at issue. </p>
  
  <p>Thus, experts may testify about the science or technology behind blood or breath tests for alcohol or drugs, fingerprints, DNA, domestic violence, mental illness, physical disabilities, firearms or ballistics, statistical probabilities, the physics of fire, causes of death, causes of injury, eye witness identification, accidents, medical procedures, etc. </p>
  
  <p>Ask your lawyer if you need an expert during your initial free consultation.</p> 
  
  <p><a name="14" class="faqAnswerHeader">How much does an expert cost?</a></p>
  
  <p>The cost of an expert depends on the type of expert, the amount of time they work on the case, the amount of time they spend in court, and any travel time.</p> 
  
 <p>Thus, a local expert who is going to testify about eyewitness identification will be much less than an exert in forensics that need to be flown in from out of state or country.</p> 
  
  <p>Ask your lawyer about the cost of an expert during your initial free consultation. </p> 

