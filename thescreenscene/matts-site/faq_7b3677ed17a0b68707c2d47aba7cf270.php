<p><a name="15" class="faqAnswerHeader">How much will my personal injury case cost?</a></p>
  
  <p>Most personal injury cases are based on a contingency fee. Your lawyer will get a percentage that is contingent on the amount your lawyer recovers. The percentage you pay your lawyer varies depending on whether your case settles or goes to trial. If your lawyer does not recover anything, your lawyer does not get paid. Regardless of whether your lawyer recovers any money, you will be responsible for any costs associated with the case. These costs include such things as: filing fees; investigation; independent medical examinations; expert testimony; and other miscellaneous expenses. </p>
  
  <p>Ask your lawyer about the percentages they will take for settlement, trial, or appeal. Also, make sure you have a clear understanding of the costs you will be responsible for. Your lawyer should include these percentages and the anticipated costs in a fee agreement.</p>
  <p>You may request an hourly fee agreement or a flat fee agreement for a personal injury case.</p>
  
  <p><a name="16" class="faqAnswerHeader">How long will my personal injury case take?</a></p>
  
  <p>A personal injury may take days, months, or years. However, most cases resolve within a year. There are many factors that determine the length of a case such as: the number of parties to the law suit; the complexity of the law; the facts of the case; the motivations of the parties to settle; or the amount of damages.</p> 
  
  <p>Ask your lawyer how long your case should take during your initial free consultation.</p> 
  
  <p><a name="17" class="faqAnswerHeader">Should I accept the offer the insurance company has made?</a></p>
  
  <p>Not before you have an attorney review their offer. Insurance adjusters are skilled negotiators who work for companies with a profit motive. Thus, it is in their interest to get you to settle for as little as possible. Sometimes an insurance adjuster will make you a fair offer. However, you should never accept an offer from an insurance company without first consulting with your lawyer. Once you accept a settlement you cannot ask for more.</p>
  
  
  <p><a name="20" class="faqAnswerHeader">I was hurt by a hit and run driver. How can I get help if I don't know who the driver was?</a></p>
  
  <p>If the person responsible for your injuries left the scene of the accident, you may still be able to recover for your injuries. In Colorado insurance companies must offer uninsured motorist /under-insured motorist coverage (UM/UIM).  This insurance covers a person in the event they are injured by an uninsured motorist, someone who has insurance insufficient to cover your injuries, or injures caused by a hit and run driver. If you have UM/UIM coverage you may be able to recover from your insurance company.</p>
  
  <p>Ask your lawyer about UM/UIM during your initial free consultation.</p>
  
  <p><a name="21" class="faqAnswerHeader">I donâ€™t have health insurance. Can you help me find a doctor to treat my injuries?</a></p>
  
  <p>Yes. It is difficult to find a doctor who will help you if you donâ€™t have health insurance. Lawyers who do personal injury work usually know doctors who will provide treatment on a lien. This means that the doctor will wait to receive payment until your case is resolved. </p>
  
  <p>One of the main goals in a personal injury case is to help the client regain his or her health. Your attorney should help you do this by helping you find treatment options.</p>
  
  <p>Ask your lawyer about doctors who treat provide treatment on a lien during your initial free consultation. </p>
  
 <p><a name="22" class="faqAnswerHeader">Should I sign the medical release from the insurance company?</a></p>
  
  <p>No. Insurance companies often give you a release that entitles them to all of your medical records from the time you were born until the present. You have a right to privacy regarding your medical records. Your lawyer will need to demonstrate the extent of your injuries to the insurance company or a jury. However, your lawyer should only disclose medical records related to the injury. You should never sign a release without first speaking with your lawyer.</p>



