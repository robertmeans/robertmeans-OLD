<p><a name="100" class="faqAnswerHeader">Do I need a lawyer for my divorce?</a></p>
<p>The law does not require you to have a lawyer. In fact, many people get divorced in Colorado without the assistance of a lawyer. However, everyone's case is different. You should consider talking to a lawyer if you and your spouse cannot agree on parenting time, spousal maintenance, child support, or other issues related to the divorce or custody. Even if you and you and your spouse agree on everything you should consider talking to a lawyer if you or your spouse have complex financial investments, own more than one property, or are the principal or owner in a corporation.</p>

<p><a name="101" class="faqAnswerHeader">What is a dissolution of marriage?</a></p>
<p>In Colorado a divorce is legally referred to as dissolution of of marriage.</p>
  
<p><a name="102" class="faqAnswerHeader">What is parenting time?</a></p>
<p>In Colorado custody is legally referred to as parenting time.</p>

<p><a name="103" class="faqAnswerHeader">I'm worried that my spouse might abduct the children and leave the state or country. What can I do?</a></p>
<p>If a petition for divorce has already been filed neither parent may leave the state with the children without the consent of the other parent. If a petition for divorcee has not been filed you, you can file a petition. However additional measures may be taken.</p>

<p>To prevent the abduction of a child a court may impose travel restrictions on a parent with the child, prevent the parent from leaving the state with the child, require a parent to surrender a U.S. or foreign passport in either the parent's or child's name.</p>

<p>Additionally a court may prevent a parent from: removing or retaining a child in violation of a child-custody determination; removing a child from school or child-  care; approaching the child at any location other than a site designated for supervised visitation; or applying for a new or replacement visa or passport.</p> 

<p>There are additional measures Colorado courts can take to ensure there is cooperation from other states, the U.S. government, and foreign countries to prevent the abduction of your child. C.R.S. 14-13.5-108.</p>

<p>You should contact a lawyer immediately if you are concerned that your spouse may abduct your child.</p>

<p><a name="104" class="faqAnswerHeader">Will my wife get more custody of the children than me?</a></p>
<p>Not simply because she is the mother of the children. Both parents have equal rights to child custody. Child custody (parenting time) is determined in the best interest of the child.</p>

<p><a name="105" class="faqAnswerHeader">I want a divorce but my spouse does not. Can I still get divorced without my spouse's consent?</a></p>
<p>Yes. In Colorado, a spouse may file for divorce without the consent of their spouse by stating the marriage is irretrievably broken.</p>

<p><a name="106" class="faqAnswerHeader">My spouse has never cheated on me, has always been supportive, and is a great parent. Can still we get divorced?</a></p>
<p>Yes. Colorado is a no-fault divorce state. This means that regardless of an affair, poor parenting, or lack of support a couple may get divorced. The only grounds you need to state for a divorce in Colorado are that the marriage is irretrievably broken.</p>

<p><a name="107" class="faqAnswerHeader">Once a divorce is filed can it be stopped?</a></p>
<p>Yes. A party may dismiss the petition for dissolution at anytime before the final decree. This is actually common, as some couples decide to reconcile before they are divorced.</p>

<p><a name="108" class="faqAnswerHeader">How much will my case cost?</a></p>
<p>The cost of legal representation varies depending on the type of case. A divorce with no children, no property, and agreement between the parties may be resolved for the cost of filing fees. In contrast, a divorce involving children, multiple properties, a family business, criminal allegations, and conflict over numerous issues may cost tens  of thousands of dollars.</p> 

<p>Much of the cost of a divorcee or custody case is determined by how many issues the parties wish to contest and the complexity of those issues.</p>

<p>Some lawyers offer an initial free consultation. Lawyers charge different rates. Speak with more than one lawyer and compare rates.</p> 

<p>Ask your lawyer what issues you may realistically win and what issues you will most likely lose. Part of good legal advice is explaining the cost of litigation and where your money is most wisely spent.</p>

<p><a name="109" class="faqAnswerHeader">My spouse cheated on me, does that mean I get the house and the kids?</a></p>
<p>No. In Colorado, it does not matter who is at fault. The court will divide marital property, allocate spousal maintenance and allocate parenting time without considering who is at fault.</p>

<p><a name="110" class="faqAnswerHeader">My spouse has assaulted/threated me and or my children how can I protect myself and/or my children?</a></p>
<p>If you have been harmed or are in fear for your safety you should call the police. If your spouse is arrested the criminal court will most likely issue a protection order.</p> 

<p>However, if the assault or threat was not that serious you may want to simply seek a civil protection order. If you call the police and your spouse is convicted of a crime, the conviction may reduce your spousesâ€™ earning potential enough to impact their ability to pay child support or spousal maintenance.</p> 

<p>A civil protection order can prohibit your spouse from contacting you or your children during the pendency of the divorce without involving the police. A civil protection order may be made permanent if a court finds, "the respondent has committed the acts constituting grounds for issuance of a civil protection order and that unless restrained will continue to commit such acts or acts designed to intimidate or retaliate against the protected person. " C.R.S 13-14-106(1)(a).</p>

<p><a name="111" class="faqAnswerHeader">My spouse is alleging that I assaulted her and/or the children. What should I do?</a></p>
<p><em>Do not talk to the police or try to explain what did or did not happen</em>. Tell the police that you would like to remain silent and would like to speak to an attorney. It is very common in a contentious divorce for one or both spouses to allege assault and or abuse. Once this happens you may be charged with a crime and a protection order may be issued.</p> 

<p><a name="112" class="faqAnswerHeader">My spouse served me with a civil protection order. What should I do?</a></p>
<p><em>Do not violate the order</em>. If your spouse secures a civil or criminal protection order, you will most likely be prohibited from contacting your spouse or perhaps even the children. If this happens do not try to contact your spouse for any reason. Do not try and communicate with your spouse through a third party. Violation of a protection order is a crime. Violation of a protection order while your divorce is pending could have a negative impact on your case.</p>

<p>You should contact a lawyer if you receive a summons. You should appear for the date on the summons. If you do not appear for the date on the summons, the court may issues a permanent protection order.</p>

<p><a name="113" class="faqAnswerHeader">What is the difference between a civil and criminal protection order?</a></p>
<p>Civil protection orders are usually issued during the pendency of a divorce. A court will issues a civil protection order if it finds, &quot;An imminent danger exists to the person or persons seeking protection under the civil protection order.&quot; C.R.S 13-14-1-104.5(7)(a). The person seeking the order does not have to report the allegations to the police. If your spouse reports an assault or abuse a temporary protection order will likely be entered. A temporary order may be made permanent if a court finds, &quot;The respondent has committed the acts constituting grounds for issuance of a civil protection order and that unless restrained will continue to commit such acts or acts designed to intimidate or retaliate against the protected person.&quot; C.R.S 13-14-  106(1)(a). In a divorce, a civil protection order will usually last until the final decree. However, it may last longer if made permanent.</p>


