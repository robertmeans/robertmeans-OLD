<p>Read the <a href="http://blogs.westword.com/latestword/2014/12/justin_hale_acquitted_murder_one_year_old.php" title="Westword article" target="_blank">article that was published in Westword here</a>.</p>


<div class="longTextContent">
<p>On November 20, 2014, a Jefferson County jury found Mr. Justin Hale not guilty of Murder
and Child Abuse Resulting in Death. The Defense’s pre-trial investigation revealed that
Brookelynn Palmer, the deceased, had a pre-existing skull fracture that had been missed by
the Westminster Police Department, medical providers at St. Anthony’s North Westminster,
a radiologist at The Children’s Hospital Aurora, and the Jefferson County District Attorney's
Office.</p>

<p>On May 18, 2013, Brookelynn Palmer suffered a skull fracture when she fell and struck her
head on a table. She was taken to The Children's Hospital Aurora where she later died of her
injuries. Neither medical professionals nor legal authorities were aware that at the time
Brookelynn Palmer had struck her head on the table, she was suffering from a pre-existing
skull fracture, making her skull more vulnerable to further injury.</p>

<p>On June 3, 2013, The Jefferson County District Attorney was unaware of the pre-existing
skull fracture when it charged Mr. Hale with First Degree Murder of a Child under the age of
12 by a Person in a Position of Trust (F1) and Child Abuse Resulting in Death (F2).
On May 2, 2013, Dorothy Palmer took Brookelynn Palmer to St. Anthony's North
Westminster and reported she had fallen down the stairs while carrying Brookelynn. Several
x-rays of Brookelynn Palmer were taken. Both the radiologist and the physician's assistant at
St. Anthony's North Westminster reviewed an x-ray of Brookelynn's head and subsequently
failed to recognize and properly diagnose the skull fracture.</p>

<p>On May 21, 2013, The Child Protection Team at The Children's Hospital Aurora was
unaware of the pre-existing skull fracture despite a review of the May 2, 2013 x-ray taken at
St. Anthony's North Westminster.</p>

<p>On May 21, 2013, a forensic pathologist from the Jefferson County Coroner's Office
performed an autopsy on Brookelynn Palmer. The examining forensic pathologist was
unaware of her pre-existing fracture because it did not appear in any of her medical records.
In October of 2013, the examining forensic pathologist from the Jefferson County Coroner's
Office found the manner of death to be a homicide.</p>

<p>During the pendency of the case, the Jefferson County District Attorney’s Office provided
the Defense with Brookelynn Palmer’s medical records and x-rays from May 2, 2013. The
Defense, upon reviewing the medical records and x-rays taken on May 2, 2013, discovered
the pre-existing skull fracture. The Defense confirmed this finding with an independent
medical expert.</p>

<p>In November of 2013, the Defense met with a radiologist from The Children’s Hospital
Aurora, a medical expert who later testified on behalf of the Jefferson County District
Attorney. During that meeting, the radiologist reviewed the May 2, 2013 x-ray with the
Defense and failed to identify the pre-existing skull fracture.</p>

<p>In May of 2014, prior to trial, the Defense disclosed its findings of the pre-existing skull
fracture to the Jefferson County District Attorney. These findings were shared with the
examining forensic pathologist.</p>

<p>After reviewing the findings of the Defense, the examining forensic pathologist from the
Jefferson County Coroner's Office changed his opinion regarding the manner of death from
homicide to undetermined because accident could not be ruled out.</p>

<p>Once disclosed by the Defense, there was no dispute amongst the prosecution's experts
regarding the presence of the pre-existing skull fracture. However, the full extent of the preexisting
fracture was unknown because the May 2, 2013 x-ray was of limited scope.</p>

<p>The Jefferson County District Attorney proceeded with a case for Murder and Child Abuse
resulting in Death despite a lack of consensus amongst their experts as to the manner of
death and an inability to medically determine the extent of the pre-existing fracture.</p>

<p>On November 24, 2014, an investigator working for The Law Firm of Matthew Beach
contacted the Jefferson County Coroner's Office. A representative of the Jefferson County
Coroner's Office advised that the manner of death was still listed as a homicide on the
official death certificate. The same representative advised that he had no knowledge of any
plan by the Coroner's Office to correct the death certificate to reflect the latest opinion of
the examining forensic pathologist.</p>

